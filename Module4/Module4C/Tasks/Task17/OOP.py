class Human:
    def __init__(self, name, age, gender):
        self.name = name
        self.age = age
        self.gender = gender

# Example of creating an instance of the Human class
humanBeing = Human("Aridunnu", 30, "Female")
print("Name:", humanBeing.name)
print("Age:", humanBeing.age)
print("Gender:", humanBeing.gender)
